package com.jorgerubira.pruebainicialgit;

public class ClasePrueba {
    public int suma(int a, int b){
        return a+b;
    }
    public int resta(int a, int b){
        return a-b;
    }  
    public int divide(int a, int b){
        return a/b;
    } 
    public int multiplica(int a, int b){
        return a*b;
    }     
}
